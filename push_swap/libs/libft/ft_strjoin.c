#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*tmp;
	size_t	len;
	size_t	i;

	if (!s1 || !s2)
		return (NULL);
	len = ft_strlen(s1) + ft_strlen(s2);
	tmp = (char *)malloc(sizeof(char) * (len + 1));
	if (!tmp)
		return (NULL);
	i = 0;
	while (*s1)
		*(tmp + i++) = *(s1++);
	while (*s2)
		*(tmp + i++) = *(s2++);
	*(tmp + i) = '\0';
	return (tmp);
}
